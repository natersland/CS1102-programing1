package data;

/**
 * Interface representing basic vehicle functionalities.
 */
public interface Vehicle {
    /**
     * Prints the model of the vehicle.
     */
    public String getModel();

    /**
     * Prints the year of the vehicle.
     */
    public int getYear();

    /**
     * Prints the manufacturer of the vehicle.
     */
    public String getManufacturer();
}
